package java_20240117;

public class Ex5 {

	public static void main(String[] args) {
		System.out.println("나무를 1번 찍었습니다.");
		for(int i = 1; i <= 9; i++) {
			System.out.println("나무를 " + i + "번 찍었습니다.");
		}
		System.out.println("=========");
		int start =1;
		while(start <= 9) {
			System.out.println("나무를 " + start + "번 찍었습니다.");
			start++;
		}

	}

}
