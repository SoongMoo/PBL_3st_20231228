package java_20240116;
public class Ex6 {
	public static void main(String[] args) {
		int i = 10;
		i++; // i = i + 1
		System.out.println(i);
		++i; // i = i + 1
		System.out.println(i);
		++i; // i = i + 1   : 증가 연산자
		i++; // i = i + 1
		System.out.println(i); // 5
		i--; // i = i - 1  : 감소 연산자
		--i;// i = i - 1
		System.out.println(i);
		--i;// i = i - 1
		--i;// i = i - 1
		System.out.println(i); // 1
	}
}
