package java_20240116;

public class Ex4 {
	public static void main(String[] args) {
		// 산술연산자 (+, -, *, /, %)
		// 증감연산자
		int i = 1;
		//i값을 1증가
		i = i + 1; // i = 1 + 1
		System.out.println(i);
		i = i + 1; // i = 2 + 1
		System.out.println(i);
		i = i + 1; 
		System.out.println(i);
		i++; // i = i + 1
		System.out.println(i);
	}
}