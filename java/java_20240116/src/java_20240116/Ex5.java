package java_20240116;

public class Ex5 {
	public static void main(String[] args) {
		int i = 1;
		// i값을 1증가
		i = i + 1;
		System.out.println(i);
		i++; // i = i + 1
		System.out.println(i);
		i++; // i = i + 1
		i++; // i = i + 1
		System.out.println(i); // 5
		i = i - 1;//  i값을 1씩 감소
		System.out.println(i); // 4
		i--; //i = i - 1
		System.out.println(i);
		i--; // i = i - 1
		i--; // i = i - 1
		System.out.println(i); // 1
	}

}
